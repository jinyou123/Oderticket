public class Flight {
    private String cfd;//出发地
    private String mdd;//目的地
    private String cfsj;//出发时间
    private String fjclx; //飞机类敞型

    public String getCfd() {
        return cfd;
    }

    public void setCfd(String cfd) {
        this.cfd = cfd;
    }

    public String getMdd() {
        return mdd;
    }

    public void setMdd(String mdd) {
        this.mdd = mdd;
    }

    public String getCfsj() {
        return cfsj;
    }

    public void setCfsj(String cfsj) {
        this.cfsj = cfsj;
    }

    public String getFjclx() {
        return fjclx;
    }

    public void setFjclx(String fjclx) {
        this.fjclx = fjclx;
    }
}
